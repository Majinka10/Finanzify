package com.finanzify.back.dto;

public class SalidaDay {
    private int cantidad;
    private int dia;

    public SalidaDay() {
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }
}
